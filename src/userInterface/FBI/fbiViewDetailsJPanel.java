    private void fraudBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fraudBtnActionPerformed
        // TODO add your handling code here:
        FBI organ=null;
        for(Enterprise enterprise:network.getEnterpriseDirectory().getEnterpriseList()){
            for(Organization org:enterprise.getOrganizationDirectory().getOrganizationList()){
                   if(org instanceof FBI)
                   organ=(FBI)org;
                   if(organ!=null)
                       break;   
                   }
            
        if(organ!=null)
           break;  
        }
        workrequest.getCertificate().setIsFraud(true);
        if(organ!=null)
              organ.getFrauds().add(workrequest);
        account.getWorkQueue().getWorkRequestList().remove(workrequest);
    }//GEN-LAST:event_fraudBtnActionPerformed

